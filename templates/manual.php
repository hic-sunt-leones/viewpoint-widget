			<h1>Handleiding 'Lens op Leiden'</h1>

			

			<p>
			Het project 'Lens op Leiden' probeert met hulp van vrijwilligers te achterhalen waar historische foto's genomen zijn. Deelnemers wordt gevraagd zowel de plek van het gefotografeerde object als de plek waar de camera stond op een kaart aan te geven.
			</p>

			<h2>Welke gegevens hebben we nodig?</h2>

			<p>Van elke foto in het project zouden we graag de volgende gegevens krijgen:</p>

			<ul>
			<li>De plek van het gefotografeerde onderwerp</li>
			<li>De plek waar de camera gestaan moet hebben</li>
			<li>De kijkhoek.</li>
			</ul>

			<p>
			Met deze gegevens kunnen we in de toekomst foto's op een kaart tonen of mensen foto's bij een bepaald gebied op de kaart laten zoeken. Doordat we het camerastandpunt weten kunnen we ook iets over de kijkrichting zeggen, bijvoorbeeld dat een foto genomen is in noord-westelijke richting. In combinatie met de kijkhoek kunnen we in veel gevallen zelfs uitrekenen welke individuele panden op de foto moeten staan.
			</p>

			<h2>Wat te doen bij twijfel?</h2>

			<p>
			Wanneer je de plek van de foto niet herkent of wanneer je twijfelt, sla je de taak over. Het systeem houdt bij welke taken je overslaat, dus je zult die specifieke taak niet nog eens voorgelegd krijgen.
			</p>

			<p>
			Ook wanneer je een gebouw wel herkent, maar niet weet welke zijde je ziet en je dus geen camerastandpunt kunt bepalen, sla je de taak over. We willen graag alle gevraagde gegevens hebben, ook als dat leidt tot wat minder opgeloste gevallen. Van niet op te lossen gevallen (parkachtige landschappen, interieurs, etc.) vragen we later in een ander project wellicht de situering meer globaal aan te geven.
			</p>

			<h2>De kaart bedienen</h2>

			<img style="width: 100%; margin-bottom: 20px;" src="<?= $baseUrl ?>assets/img/legenda.png" alt="" />

			<h3>inzoomen en uitzoomen</h3>

			<p>
			Inzoomen kan door te klikken op het icoontje met het plusje (+), of door op de kaart te dubbelklikken. Extra snel zoom je in door de shift-toets ingedrukt te houden en tegelijkertijd een rechthoekje op de kaart te trekken.
			</p>

			<p>
			Uitzoomen doe je door op het icoontje met het minnetje (-) te klikken of door op de kaart te dubbelklikken terwijl je de shift-toets ingedrukt houdt.
			</p>

			<h3>camerastandpunt en gefotografeerd onderwerp aangeven</h3>

			<p>
			Op de kaart versleep je het cameraatje, dat bij het laden van de pagina in het midden van de kaart staat, naar de plek waar de camera gestaan moet hebben. Om te kunnen slepen klik je op het cameraatje en houdt je je muisknop ingedrukt.
			</p>

			<p>
			Het gefotografeerde object wordt aangegeven met het bolletje dat aan weerszijden een pijltje heeft. Je pakt het bolletje op en sleept het (muisknop ingedrukt houden) naar de plaats waar het gefotografeerde object zich bevond of bevindt.
			</p>


			<h3>kijkhoek instellen</h3>

			<p>
			Onder in de kaart zie je een slider waarmee je de kijkhoek aan kan geven. Is de foto genomen met een telelens of juist een groothoek? Kijk bijvoorbeeld welke panden op de foto te zien zijn en stel de kijkhoek zo in dat deze panden binnen het blauwgrijze driekhoekje vallen.
			</p>

			<h3>camera / onderwerp naar midden kaart verplaatsen</h3>

			<p>
			Als je, bijvoorbeeld na inzoomen, zowel cameraatje als object-bolletje niet meer op de kaart ziet, kan je op de knop 'beweeg camera naar huidig kaartvenster' drukken. Dit is de derde knop van boven, onder het plusje en minnetje. Het cameraatje en bolletje worden dan in het midden van de foto geplaatst. 
			</p>

			<p>Sneltoets 'c' doet hetzelfde.</p>

			<p>
			Let op, ze behouden daarbij hun onderlinge afstand, dus het kan voorkomen dat ze te ver uiteen liggen om samen op een ingezoomde kaart te tonen. In zo'n geval moet je eerst uitzoomen, camera en bolletje dichter bij elkaar plaatsen, en dan weer inzoomen.
			</p>


			<h3>kaart verplaatsen naar laatst aangegeven camera / onderwerp</h3>

			<p>
			Wil je terug naar de laatste positie waarin je het cameraatje en object-bolletje hebt geplaatst, druk dan op 'verplaats kaart naar camera'. Het kaartvenster zoomt dan in op de plek waar camera en object-bolletje al stonden.
			</p>

			<p>Sneltoets 'm' doet hetzelfde.</p>

			<h2>Hulpmiddelen</h2>

			<p>
			Behalve de foto zelf zijn meestal wel enige gegevens bekend  (zoals de collectie waar de foto uit komt, een titel, jaar of beschrijving) die kunnen helpen bij het plaatsen van camera en onderwerp. Daarnaast zijn bij vergelijkbare projecten de volgende bronnen handig gebleken.
			</p>

			<h3>geografie / kaarten</h3>

			<p>
			Google Maps (denk ook aan de satellietview) is handig. Streetview is dat zeker ook - vaak zijn nog bestaande panden op de foto in streetview wel thuis te brengen.
			</p>

			<p>
			Voor contemporain kaartmateriaal kan je bij het archief terecht. <a href="">Topotijdreis</a> geeft een landdekkend beeld van de veranderingen van de afgelopen anderhalve eeuw.
			</p>

			<h3>andere foto's</h3>

			<p>
			Zeker als de plek ongeveer bekend is, kan het verhelderend zijn om andere foto's van dezelfde omgeving (straat) in de ELO beeldbank te bekijken. Gevelindelingen en details kunnen aanknopingspunten geven. Soms kom je op foto's zelfs een huisnummer tegen. In combinatie met streetview ontstaat vaak een goed beeld van waar een afgebeeld gebouw precies stond.
			</p>

